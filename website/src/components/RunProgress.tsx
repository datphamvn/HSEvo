const opOf = (iter: number): OpKey => {
  if (iter === 1) return "init";
  const m = iter % 3;
  if (m === 2) return "crossover";
  if (m === 0) return "mutation";
  return "hs";
};

// Selection is a phase (it produces no individuals) that starts each round,
// right before a crossover iteration. We render it on the transition arrows so
// it is separated from the harmony search operator.
const SELECTION = {
  label: "Selection",
  stroke: "stroke-rose-500 dark:stroke-rose-400",
  swatch: "bg-rose-500",
  border: "border-rose-500 dark:border-rose-400",
};

// The HSEvo pipeline steps, shown beside the chart and highlighted in sync with
// the current operator. `phase` links a step to the operator iteration during
// which it is active ("selection" steps are active during crossover rounds,
// "always" steps run every iteration).
type Phase = OpKey | "selection" | "always";

type Detail = { type: "code" | "text"; content: string };

type StepDef = {
  title: string;
  description: string;
  phase: Phase;
  input?: Detail;
  output?: Detail;
};

