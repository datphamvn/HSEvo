// Real HSEvo run on the BPP online problem (lower objective is better).
// Each individual is [iteration, objective]. In HSEvo's loop every iteration
// is a single operator stage, so we can attribute each individual to the
// operator that produced it:
//   iter 1            -> initialization
//   iter % 3 === 2    -> crossover      (2, 5, 8, ...)
//   iter % 3 === 0    -> elitist mutation (3, 6, 9, ...)
//   iter % 3 === 1 >1 -> harmony search (4, 7, 10, ...)

const MAX_ITER = Math.max(...RUN_DATA.map(([i]) => i));

